"""
Ranges em Python
"""


# RANGE INICIO DE 0 até 11
for num in range(11):
    print(num)

# RANGE INICIO DE 5 até 11
for num in range(5,11):
    print(num)
# RANGE INICIO DE 5 até 10 passo 2 em 2
for num in range(5,10,2):
    print(num)
# RANGE INICIO DE 20 até 5 passo menos 2 em 2
for num in range(20,5,-2):
    print(num)




